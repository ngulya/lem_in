#include "lem.h"

char	*ft_strndup(const char *s1, int i)
{
	char	*a;
	char	*tmp;
	int		s;

	s = i;
	a = (char *)malloc(i + 1);
	tmp = a;
	*(a + i) = '\0';
	while (s > 0)
	{
		*a = *s1;
		a++;
		s1++;
		s--;
	}
	return (tmp);
}
